                    <h2 class="aboutus-title">TDS SERVICES</h2>

                    <div class="top-head-text">

                        <p>The deduction of tax at source or TDS has been very helpful in the collection of taxes in the country by targeting the source of income itself. It is useful to both the government and taxpayers. When it comes to the government, it helps control evasion to a certain extent by placing the liability on most business entities to deduct tax in respect of certain expenses if they cross the specified threshold/limit. Further, it also keeps the inflow of tax running throughout the year. For taxpayers, it eases their burden of paying tax when it is time to file their income tax returns. That is because, they get credit for the taxes deducted at source.</p>

<p>One of the most important and common type of payments that a business entity makes is towards professional fees or fees for technical services. Some illustrative examples of professional fees are fees paid to a lawyer, doctor, engineer, architect, chartered accountant, interior decorators, advertisers, etc. Technical services would include the rendering of managerial, technical or consultancy services.</p>     

</div>

<h3>Types of payments covered</h3>  



<p>The type of payments covered under this section are as follows:</p>



<p>1. Professional fees</p>



<p>2. Fees for technical services</p>



<p>3. Remuneration paid to directors excluding salary (For e.g., sitting fees to attend board meetings)</p>



<p>4. Royalty</p>



 <h3>Threshold limit for deducting tax</h3>



<p>Tax has to be deducted in case the payment is greater than Rs. 30,000 during the year. However, there is no such limit for payments made to a director. The tax will have to be deducted no matter how small the amount.</p>



<h3>Persons liable to deduct tax</h3>



<p>Every person, who is making a payment in the nature of fees for professional or technical services is liable to deduct tax at source with the following exceptions:</p>



<p>1. In case of an individual or HUF carrying on business: Where his turnover does not exceed Rs. 1 crore during the previous financial year.</p>



<p>2. In case of an individual or HUF carrying on profession: Where his turnover does not exceed Rs. 50 lakh during the previous financial year.</p>



<p>To put it simply, all entities (other than individuals/HUF who are not required to do tax audit in the preceding year) need to deduct tax. </p>  





<h3>Rate of deduction of tax</h3>



<p>Any payment covered under this section shall be subject to TDS at the rate of 10%.</p>



<p>However, w.e.f. 01.04.2017, the tax on payments made to operators of call centres shall be deducted at a reduced rate of 2%.</p>



<p>In case the payee does not furnish his PAN then the rate of deduction would be 20%.</p>



<h3>Time of deduction</h3>



<p>The tax should be deducted at the time of passing such entry in the accounts or making the actual payment of the expense, whichever earlier.</p>



<h3>Consequences of non-deduction or late deduction</h3>



<p>Not deducting the tax or late deduction of the tax has a two-fold consequence:</p> 



<p>1. Disallowance of a part of the expenditure: 30% of the expenditure shall be disallowed in the year in which the expenditure is claimed (taken to the profit and loss account) – however, the 30% disallowed shall be re-allowed in the year in which the TDS is paid to the government.</p>



<p>2. Levy of interest until date of payment: In case there is a delay in the payment of tax, interest has to be paid along with the TDS to the Government. The rate of interest is determined in the following manner:</p>



<p>a. Where no deduction of tax has been made: Interest shall be payable at 1% per month/part of month from the date on which such tax was required to be deducted up to the date of actual deduction.</p>



<p>b. Where tax has been deducted but not paid to the government: Interest shall be payable at 1.5% per month/part of month from the date on which such tax was deducted up to the date of payment to the government.</p>   



