                    <h2 class="aboutus-title">GST ADVISORY SERVICES</h2>

<div class="top-head-text">
                        <p>GST is an Indirect Tax which has replaced many Indirect Taxes in India. The Goods and Service Tax Act was passed in the Parliament on 29th March 2017. The Act came into effect on 1st July 2017; Goods & Services Tax Law in India is a <strong>comprehensive</strong>, <strong>multi-stage</strong>, destination-based tax that is levied on every <strong>value addition</strong>.</p>

					
<p>In simple words, Goods and Service Tax (GST) is an indirect tax levied on the supply of goods and services. This law has replaced many indirect tax laws that previously existed in India.</p>

<p>GST is <strong>one indirect</strong> tax for the <strong>entire country</strong>.</p>	


<p>The GST journey began in the year 2000 when a committee was set up to draft law. It took 17 years from then for the Law to evolve. In 2017 the GST Bill was passed in the Lok Sabha and Rajya Sabha. On 1st July 2017 the GST Law came into force.</p>

</div>



<h3>GST Advantages:</h3>



<p>GST has mainly removed the Cascading effect on the sale of goods and services. Removal of cascading effect has impacted the cost of goods. Since the GST regime eliminates the tax on tax, the cost of goods decreases.</p>



<p>GST is also mainly technologically driven. All activities like registration, return filing, application for refund and response to notice needs to be done online on the GST Portal; this accelerates the processes.</p>

	

<p>1. Removing cascading tax effect</p>



<p>2. Online simpler procedure under GST</p>



<p>3. Lesser Compliances</p>



<p>4. Defined treatment for e-commerce</p>



<p>5. Composition scheme for small business</p>



<p>6. Increased efficiency in logistics</p>



<p>7. Higher threshold for registration etc.</p>



<h3>Components of GST:</h3>



<p>There are 3 taxes applicable under this system: CGST, SGST & IGST.</p>



<p>1. <strong>CGST:</strong> Collected by the Central Government on an intra-state sale (Eg: transaction happening within home state)</p>



<p>2. <strong>SGST:</strong> Collected by the State Government on an intra-state sale (Eg: transaction happening within home state)</p>



<p>3. <strong>IGST:</strong> Collected by the Central Government for inter-state sale (Eg: home state to Other state)</p>



<h3>Post GST:</h3>



<p>In the pre-GST regime, every purchaser including the final consumer paid tax on tax. This tax on tax is called Cascading Effect of Taxes.</p>



<p>GST has removed this cascading effect as the tax is calculated only on the value-addition at each stage of the transfer of ownership. This indirect tax system under GST has improved the collection of taxes as well as boosted the development of Indian economy by removing the indirect tax barriers between states and integrating the country through a uniform tax rate.</p>



<h3>Decisions at 37th GST council meeting</h3>



<p>The meeting concluded with several decisions ranging from relaxations in annual return filing, rate revisions, deferment of new GST returns, changes to composition scheme, new GST exemptions and some clarifications detailed as follows:</p>



<h3>1. Waiver of GSTR-9A for Composition Taxpayers for FY 2017-18 & FY 2018-19</h3>



<p>Composition dealers have been granted exemption from filing of annual returns in GSTR-9A for the fiscal years 2017-18 and 2018-19. In the future, it is mostly expected to be done away since the form GSTR-4 has been tweaked to allow an annual declaration of turnover and tax details.</p>



<h3>2. GSTR-9 for small taxpayers now not compulsory for FY 2017-18 & FY 2018-19 </h3>  



<p>Those taxpayers having an annual turnover of up to Rs 2 crore in FY 2017-18 or FY 2018-19, may choose to not file GSTR-9, from the date to be notified by CBIC. GST Council has also decided to review the simplification of GSTR-9, 9A and 9C forms and filing. However, they must continue to file annual returns for FY 2019-2020 onwards.</p>



<p>Large taxpayers should continue to comply and close annual return filing both in GSTR-9 and GSTR-9C by 30 November 2019.</p>



<h3>3. New GST Returns Deferred to April 2020</h3>



<p>The new GST return system will now be implemented from April 2020. This decision is appreciated as several transitional issues that could crop up while implementing in the middle of the year, can now be avoided. Taxpayers can begin on a fresh note from a new financial year. It must be noted that the decision to defer applies to all taxpayers and for all forms under the new GST returns system.</p>



<h3>4. Restrictions on ITC claim in GSTR-3B</h3>



<p>To push the timely filing of a statement of outward supplies by taxpayers, the GST Council recommended restrictions on Input Tax Credit (ITC) claim. ITC will be restricted for the recipients if the suppliers have not furnished the details of outward supplies.</p>



<h3>5. Circular on Post Sale Discount Withdrawn</h3>



<p>CBIC released Central Tax Circular number 105, dated 28 June 2019. Through the circular, they have clarified post-sale discount for promotional activities by the dealer, secondary discounts, and reversal of ITC on post-sale discount.</p>



<p>The circular clarifies that whether the post-sale discount has to be included in the value of supply or not and whether ITC is to be reversed at the time of issuing a credit note or not in certain specified cases.

The GST Council recommended revoking of the circular number 105 with retrospective is effective from the beginning (ab-initio).</p>



