                        <h3>Accounts Payable</h3>
                        <p >Accounts payable and its management is a critical business process through which an entity manages its payable obligations effectively. Accounts payable is the amount owed by an entity to its vendors/suppliers for the goods and services received. To elaborate, once an entity orders goods and receives before making the payment for it, it should record a liability in its books of accounts based on the invoice amount. This short-term liability due to the suppliers, vendors, and others is called accounts payable. Once the payment is made to the vendor for the unpaid purchases, the corresponding amount is reduced from the accounts payable balance.</p>
                        
                        <h3>Accounts Receivable</h3>
                        <p>Accounts Receivable refers to the outstanding invoices a company has, or the money it is owed from its clients. In your personal life, an example of Accounts Receivable would be buying a ticket to a concert or sporting event for a friend with the understanding that they will pay you back later.  It’s essentially an “IOU”. In business, AR represents a line of credit extended by a company, due within a relatively short timeframe, which could range from a few days to a year.</p>
                        <h3>Bank Reconciliation Statement</h3>
                        <p>A bank reconciliation is the process of matching the balances in an entity's accounting records for a cash account to the corresponding information on a bank statement. The goal of this process is to ascertain the differences between the two, and to book changes to the accounting records as appropriate. The information on the bank statement is the bank's record of all transactions impacting the entity's bank account during the past month.<br />
<br />


A bank reconciliation should be completed at regular intervals for all bank accounts, to ensure that a company's cash records are correct. Otherwise, it may find that cash balances are much lower than expected, resulting in bounced checks or overdraft fees. A bank reconciliation will also detect some types of fraud after the fact; this information can be used to design better controls over the receipt and payment of cash</p>
