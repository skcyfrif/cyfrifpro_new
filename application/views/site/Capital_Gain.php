<h3>Capital Assets</h3>

<p>Land, building, house property, vehicles, patents, trademarks, leasehold rights, machinery, and jewellery are a few examples of capital assets. This includes having rights in or in relation to an Indian company. It also includes the rights of management or control or any other legal right.</p>


<h3>Capital Assets Two Types:</h3>
 
<p>1. Short-term capital asset An asset held for a period of 36 months or less is a short-term capital asset. The criteria of 36 months have been reduced to 24 months for immovable properties such as land, building and house property from FY 2017-18.</p>

<p>For instance, if you sell house property after holding it for a period of 24 months, any income arising will be treated as long-term capital gain provided that property is sold after 31st March 2017.</p>

<p>2. Long-term capital asset An asset that is held for more than 36 months is a long-term capital asset. The reduced period of the aforementioned 24 months is not applicable to movable property such as jewellery, debt-oriented mutual funds etc. They will be classified as a long-term capital asset if held for more than 36 months as earlier.</p>

<p>Some assets are considered short-term capital assets when these are held for 12 months or less. This rule is applicable if the date of transfer is after 10th July 2014 (irrespective of what the date of purchase is).</p>

<h3>The assets are:</h3>

<p>a. Equity or preference shares in a company listed on a recognized stock exchange in India</p>

<p>b. Securities (like debentures, bonds, govt securities etc.) listed on a recognized stock exchange in India</p>

<p>c. Units of UTI, whether quoted or not</p>

<p>d. Units of equity oriented mutual fund, whether quoted or not</p>

<p>e. Zero coupon bonds, whether quoted or not</p>

<p>When the above-listed assets are held for a period of more than 12 months, they are considered as long-term capital asset.</p>

<p>In case an asset is acquired by gift, will, succession or inheritance, the period for which the asset was held by the previous owner is also included when determining whether it's a short term or a long-term capital asset. In the case of bonus shares or rights shares, the period of holding is counted from the date of allotment of bonus shares or rights shares respectively.</p>

