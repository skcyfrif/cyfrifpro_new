<h3>Change in Tax Rules for Debt Mutual Funds</h3>

<p>Debt mutual funds have to be held for more than 36 months to qualify as a long-term capital asset. It means you need to remain invested in these funds for at least three years to get the benefit of long-term capital gains tax. If redeemed within three years, the capital gains will be added to your income and will be taxed as per your income tax slab rate.</p>
 

<h3>The following do not come under the category of capital asset:</h3>

 

<p>a. Any stock, consumables or raw material, held for the purpose of business or profession</p>

<p>b. Personal goods such as clothes and furniture held for personal use</p>

<p>c. Agricultural land in rural India</p>

<p>d. 6½% gold bonds (1977) or 7% gold bonds (1980) or national defence gold bonds (1980) issued by the central government</p>

<p>e. Special bearer bonds (1991)</p>

<p>f. Gold deposit bond issued under the gold deposit scheme (1999) or deposit certificates issued under the Gold Monetisation Scheme, 2015</p>

