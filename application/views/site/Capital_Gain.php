<p>
Any profit or gain that arises from the sale of a 'capital asset' is a capital gain. This gain or profit is comes under the category 'income', and hence you will need to pay tax for that amount in the year in which the transfer of the capital asset takes place. This is called capital gains tax, which can be short-term or long-term.</p>

<p>Capital gains are not applicable to an inherited property as there is no sale, only a transfer of ownership. The Income Tax Act has specifically exempted assets received as gifts by way of an inheritance or will. However, if the person who inherited the asset decides to sell it, capital gains tax will be applicable.</p>
