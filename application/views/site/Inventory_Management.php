                    <h2 class="aboutus-title">Inventory Management</h2>
                       
<p>Material is a substance (Physics term) that forms part of or composed of a finished product. i.e material refers to the commodities supplied to an undertaking for the purpose of consumption in the process of manufacturing or of rendering service or for transformation into products. The term ‘Stores’ is often used synonymously with materials, however, stores has a wider meaning and it covers not only raw materials consumed but also such other items as sundry supplies, maintenance stores, fabricated parts, components, tools, jigs, other items, consumables, lubricants......etc</p>

<p>Finished and partly finished products are also often included under the term ‘Stores’. Materials are also known as Inventory. The term Materials / Inventory covers not only raw materials but also components, workin-progress and finished goods and scrap also. Material cost is the significant constituent of the total cost of any product. It constitutes 60% to 80% of the total cost. But for production  sector the material costs are of greatest significance. Inventory also constitutes a vital element in the Working Capital. So it is treated as equivalent to cash. Therefore the analysis and control on Material Cost is very important.</p>

<p>Materials constitute a significant part for manufacturing of products in an organisation. Being an input and a significant element of cost, it requires proper attention of the management because the cost control begins from here</p>

